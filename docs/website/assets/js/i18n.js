function normalizeLanguage(language) {
  if (!language) return DEFAULT_LANGUAGE;
  if (SUPPORTED_LANGUAGES.includes(language)) return language;
  const lower = String(language).toLowerCase();
  if (lower.startsWith("zh-tw") || lower.startsWith("zh-hk") || lower.startsWith("zh-hant")) return "zh-TW";
  if (lower.startsWith("zh")) return "zh-CN";
  if (lower.startsWith("en")) return "en";
  return DEFAULT_LANGUAGE;
}

function getInitialLanguage() {
  const stored = localStorage.getItem("mnemo_lang");
  return normalizeLanguage(stored || navigator.language || DEFAULT_LANGUAGE);
}

