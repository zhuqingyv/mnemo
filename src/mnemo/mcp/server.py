@mcp.tool
@monitor_tool()
async def archive_knowledge(
    id: int | None = None,
    reason: str | None = None,
    task_id: str | None = None,
) -> str:
    """Archive a knowledge entry — 把 status 从 "active" 改成 "archived"，并
    写入一条审计事件。归档不是删除，条目仍然可以用 get_knowledge /
    get_related 取出，也能通过 unarchive_knowledge 恢复；只是默认从
    search 里藏起来，减少噪声。

    典型使用时机：
    - 知识被更新更准确的条目取代（新条目的 supersedes_id 已经指向它），
      旧版不再需要出现在搜索里。
    - 外部事实变了，整条知识不再成立 —— 和 "outdated" feedback 不同，
      outdated 说的是"需要修"，archive 说的是"不用再看了"。
    - 项目/模块下线，相关知识失去上下文。
    - 用户或团队决定"这条结论作废"，哪怕代码层面还能跑。

    归档前：
    - 最好先 search 一下，确认没有其它文档正引用这条 title。
    - 给一个清晰的 reason —— "superseded by #123"、"module removed in
      2026-04"、"decision reversed in team meeting" 都比空着好。审计事件
      会把 reason 一起写下来，将来排查决策变迁时要靠它。

    不要归档：
    - 只是临时不想在搜索里看见 —— 那是 tag / scope 的职责，不是归档。
    - 知识只是"不全"或"该更新" —— 走 update_knowledge。
    - 还没 supersede 的旧决策在归档前先确认：未来真的不会再被引用吗？
      如果有"历史决策为什么当时是这样"的追溯价值，保留并补一条说明更好。

    要恢复：unarchive_knowledge(id) 即可，反向操作。

    参数：
    - id：必填。但在 state_machine_enabled=False 时会被 feature flag 先
      拦截并返回 {"success": False, "reason": "feature_disabled"}。
    - reason：可选但强烈建议填。
    - task_id：可选，搜索结果附带的任务 ID，原样传回即可。
      系统据此区分搜索派发归档 vs agent 自发归档，用于完成率统计。

    ``id`` is nominally required; declared optional so the state-machine
    feature flag can veto the call before argument validation
    (see ``tests/test_feature_flags.py`` Constraint 4).
    """
    service = _require_service()
    cfg = getattr(service, "_config", None)
    if cfg is not None and not getattr(cfg, "state_machine_enabled", True):
        record_payload(knowledge_id=id, status="skipped", feature_disabled=True)
        return json.dumps({"success": False, "reason": "feature_disabled"})
    if id is None:
        record_payload(status="error", error_type="missing_id")
        return json.dumps({"success": False, "reason": "missing_id"})
    clean_task_id, trigger_source = _validate_task_id(task_id)
    result = await service.archive_knowledge(
        id,
        reason=reason,
        task_id=clean_task_id,
        trigger_source=trigger_source,
    )
    success = bool(result.get("success"))
    record_payload(
        knowledge_id=id,
        success=success,
        has_reason=bool(reason),
        failure_reason=result.get("reason") if not success else None,
    )
    if not success:
        err = result.get("reason") or result.get("error") or "unknown error"
        return f"Archive failed for id {id}: {err}"
    parts = [f"Archived knowledge id {id}."]
    if reason:
        parts.append(f"Reason: {reason}")
    return "\n".join(parts)


@mcp.tool
@monitor_tool()
async def unarchive_knowledge(id: int) -> str:
    """Restore an archived knowledge entry (status='archived' → 'active')."""
    service = _require_service()
    result = await service.unarchive_knowledge(id)
    success = bool(result.get("success"))
    record_payload(knowledge_id=id, success=success)
    if not success:
        err = result.get("error") or "unknown error"
        return f"Unarchive failed for id {id}: {err}"
    return f"Unarchived knowledge id {id}."


@mcp.tool
@monitor_tool()
async def feedback_knowledge(
    knowledge_id: int,
    signal: str,
    reason: str | None = None,
    actor: str = "agent:unknown",
    task_id: str | None = None,
) -> str:
    """Record agent feedback on a knowledge entry. 这是 search / get_knowledge
    之后的收尾动作 —— 用过一条知识，就得留下一次反馈，知识库才知道哪些
    条目在真实工作里"扛得住"。不反馈等于白用。

    三种 signal 的判断：
    - "helpful"：这条知识直接帮你回答/解决了当前问题，不需要改写。
    - "misleading"：这条知识和当下场景不符、结论有偏差、或 Why 没解释清楚
      导致你一开始走错了。reason 里写明"哪里误导了"，下次才能修。
    - "outdated"：内容曾经对，但已被新决策/新代码/新外部约定推翻。
      典型信号是"这里提到的文件/函数/接口不存在了"。之后一般跟着
      update_knowledge 或 archive_knowledge。

    什么时候反馈：
    - 在回答用户 **之后**、而不是读到知识的瞬间 —— 要用真实结果判断。
    - 每次搜索命中并用到的条目都该反馈，哪怕只是 helpful。
    - 对于摘要不够又没点开 get_knowledge 的条目，不用反馈。

    什么时候不反馈：
    - 只是扫了一眼 search 结果、没真的用 —— 不要强行刷好评。
    - 24 小时内对同一条 (actor, knowledge_id) 已经反馈过 —— 有去重锁，
      重复调用会被拒。

    参数：
    - signal："helpful" / "misleading" / "outdated"。
    - reason：自由文本，建议写具体，空写也行但信息量低。
    - actor：调用方身份（用于 24h dedup 的去重键）。默认 "agent:unknown"，
      多 agent 场景建议显式传 "agent:<role>"。
    - task_id：可选，搜索结果附带的任务 ID，原样传回即可。
      系统据此区分搜索派发反馈 vs agent 自发反馈，用于完成率统计。

    当 feedback_loop_enabled=False 时返回
    {"success": False, "reason": "feature_disabled"} 且不写事件。
    """
    service = _require_service()
    cfg = getattr(service, "_config", None) or MnemoConfig()
    if not getattr(cfg, "feedback_loop_enabled", True):
        record_payload(
            actor=actor,
            knowledge_id=knowledge_id,
            signal=signal,
            status="skipped",
            feature_disabled=True,
        )
        return json.dumps({"success": False, "reason": "feature_disabled"})

    clean_task_id, trigger_source = _validate_task_id(task_id)
    result = await service.feedback_knowledge(
        knowledge_id=knowledge_id,
        signal=signal,
        reason=reason,
        actor=actor,
        task_id=clean_task_id,
        trigger_source=trigger_source,
    )
    record_payload(
        actor=actor,
        knowledge_id=knowledge_id,
        signal=signal,
        success=bool(result.get("success")),
        deduped=bool(result.get("deduped") or result.get("already_recorded")),
        reason_len=len(reason) if reason else 0,
    )
    if signal == "misleading" and result.get("success"):
        try:
            async with service._session_factory() as detect_session:
                for task in await health_detectors.detect_high_misleading(
                    detect_session, knowledge_id
                ):
                    _add_health_task(task)
        except Exception as e:  # noqa: BLE001
            logger.warning(
                "health P1-1 detector failed kid=%s: %s", knowledge_id, e
            )
    payload = json.dumps(result, default=str)
    hint = hint_from_feedback_result(result, knowledge_id)
    if hint:
        payload += hint
    return payload


@mcp.tool
@monitor_tool()
async def get_related(id_or_title: str, depth: int = 1) -> str:
    """Traverse the relation graph from a knowledge entry.

    - depth: how many hops to follow (default 1)
    """
    service = _require_service()
    key = _parse_id_or_title(id_or_title)
    neighbors = await service.get_related(key, depth=depth)
    record_payload(
        hits=len(neighbors),
        depth=depth,
        lookup_key_type="id" if isinstance(key, int) else "title",
        knowledge_id=key if isinstance(key, int) else None,
    )
    if not neighbors:
        return f"No related knowledge found for {id_or_title} (depth={depth})."
    lines = [
        f"## Related knowledge for {id_or_title} (depth={depth})",
        f"_{len(neighbors)} neighbor(s)_",
        "",
    ]
    for i, n in enumerate(neighbors, start=1):
        lines.append(_format_summary_entry(i, n))
    return "\n".join(lines)


@mcp.tool
@monitor_tool()
async def list_tags(scope: str | None = None) -> str:
    """List every distinct tag, optionally filtered by scope."""
    service = _require_service()
    tags = await service.list_tags(scope=scope)
    record_payload(tag_count=len(tags), scope=scope)
    if not tags:
        scope_label = f" in scope '{scope}'" if scope else ""
        return f"No tags found{scope_label}."
    header = "## All tags" + (f" (scope: {scope})" if scope else "")
    return header + "\n" + "\n".join(f"- {t}" for t in tags)


@mcp.tool
@monitor_tool()
async def search_by_tag(
    tags: str,
    scope: str | None = None,
    limit: int = 20,
) -> str:
    """Find knowledge entries that carry ALL of the given tags.

    - tags: comma-separated list (e.g. "python,async")
    """
    service = _require_service()
    tag_list = _split_csv(tags)
    if not tag_list:
        record_payload(hits=0, tag_count=0, scope=scope, status="error", error_type="empty_tags")
        return "No tags provided."
    hits = await service.search_by_tag(tag_list, scope=scope, limit=limit)
    record_payload(hits=len(hits), tag_count=len(tag_list), scope=scope)
    if not hits:
        return f"No knowledge found with tags: {', '.join(tag_list)}"
    lines = [
        f"## Knowledge with tags: {', '.join(tag_list)}  ({len(hits)} hits)",
        "",
    ]
    for i, h in enumerate(hits, start=1):
        lines.append(_format_summary_entry(i, h))
    return "\n".join(lines)


