VALID_CLAIM_TYPES: frozenset[str] = frozenset(
    {"fact", "decision", "procedure", "hypothesis"}
)


# Phase 5 task-tracking: task_id is a 32-char lowercase hex uuid4 generated by
# ``HealthTask`` and piggy-backed on search hints. The MCP layer validates the
# shape locally; if ``mnemo.health.tracking`` exposes its own validator we
# defer to it so the two stay in lockstep.
_TASK_ID_RE = re.compile(r"^[0-9a-f]{32}$")


def _validate_task_id(task_id: str | None) -> tuple[str | None, str]:
    """Return ``(clean_task_id, trigger_source)``.

    * Valid hex uuid → (task_id, "search_dispatch")
    * None / invalid  → (None, "agent_initiative")

    Invalid values are silently dropped (never raises) per TASK_TRACKING §3:
    agents must never break because of a malformed tracking token.
    """
    if task_id is None:
        return None, "agent_initiative"
    try:  # prefer the shared validator when tracking module is ready
        from mnemo.health import tracking as _tracking  # type: ignore

        validator = getattr(_tracking, "validate_task_id", None)
        if validator is not None and validator(task_id):
            return task_id, "search_dispatch"
    except Exception:  # noqa: BLE001
        pass
    if isinstance(task_id, str) and _TASK_ID_RE.fullmatch(task_id):
        return task_id, "search_dispatch"
    return None, "agent_initiative"


