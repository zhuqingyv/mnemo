_service: KnowledgeService | None = None


def set_service(service: KnowledgeService) -> None:
    """Inject the KnowledgeService — used by both the CLI entrypoint and tests."""
    global _service
    _service = service


def _require_service() -> KnowledgeService:
    if _service is None:
        raise RuntimeError(
            "KnowledgeService not initialized — call set_service() before invoking tools"
        )
    return _service


