def _split_csv(value: str | None) -> list[str]:
    if not value:
        return []
    return [part.strip() for part in value.split(",") if part.strip()]


def _sanitize_claim_type(value: str | None) -> str | None:
    """Whitelist-validate claim_type coming from an agent over MCP.

    Why: agents occasionally paste XML tool-call fragments (e.g.
    ``decision</claim_type>\\n<parameter name=...``) into the value slot
    when hand-producing arguments. Those shards must not reach storage —
    they break downstream rerank (freshness uses claim_type to pick
    lambda) and any filtering by type. Missing / blank stays None so the
    "unspecified" case still works.
    """
    if value is None:
        return None
    trimmed = value.strip()
    if not trimmed:
        return None
    if trimmed in VALID_CLAIM_TYPES:
        return trimmed
    logger.warning(
        "create_knowledge: rejecting invalid claim_type=%r (not in %s); storing as None",
        value,
        sorted(VALID_CLAIM_TYPES),
    )
    return None


def _parse_id_or_title(value: str) -> int | str:
    v = value.strip()
    if v.isdigit():
        return int(v)
    return v


def _format_tags(tags: list[str]) -> str:
    return ", ".join(tags) if tags else "_(none)_"


def _format_summary_entry(index: int, item: dict[str, Any]) -> str:
    claim_type = item.get("claim_type") or "_(unspecified)_"
    status = item.get("status") or "active"
    version = item.get("version")
    status_line = f"{status} · v{version}" if version is not None else status
    conflicts = item.get("conflicts_with") or []

    lines = [
        f"### {index}. {item['title']} (id: {item['id']})",
        f"- **Type:** {claim_type}  ·  **Status:** {status_line}",
        f"- **Tags:** {_format_tags(item.get('tags') or [])}",
        f"- **Scope:** {item.get('scope', 'global')}"
        + (f" / {item['project_name']}" if item.get("project_name") else ""),
    ]
    if conflicts:
        conflict_ids = ", ".join(f"#{cid}" for cid in conflicts)
        lines.append(f"- **Conflicts with:** {conflict_ids}")
    lines.append(f"- **Summary:** {item.get('summary') or '_(empty)_'}")
    return "\n".join(lines) + "\n"


def _format_full_entry(item: dict[str, Any]) -> str:
    related = item.get("related") or []
    claim_type = item.get("claim_type") or "_(unspecified)_"
    status = item.get("status") or "active"
    version = item.get("version")
    status_line = f"{status} · v{version}" if version is not None else status
    parts = [
        f"# {item['title']} (id: {item['id']})",
        "",
        f"- **Type:** {claim_type}  ·  **Status:** {status_line}",
        f"- **Tags:** {_format_tags(item.get('tags') or [])}",
        f"- **Scope:** {item.get('scope', 'global')}"
        + (f" / {item['project_name']}" if item.get("project_name") else ""),
        f"- **Source:** {item.get('source') or '_(none)_'}",
        f"- **Related:** {', '.join(related) if related else '_(none)_'}",
        f"- **Created:** {item.get('created_at') or '_(unknown)_'}",
        f"- **Updated:** {item.get('updated_at') or '_(unknown)_'}",
        "",
        "## Summary",
        item.get("summary") or "_(empty)_",
        "",
        "## Content",
        item.get("content") or "_(empty)_",
    ]
    return "\n".join(parts)


