async def _bootstrap() -> None:
    config = MnemoConfig()
    await init_db(config)
    factory = get_session_factory(config)
    from mnemo.services.embedding_service import EmbeddingService
    embedding = EmbeddingService(config)
    set_service(KnowledgeService(session_factory=factory, embedding_service=embedding))
    configure_monitor(session_factory=factory, enabled=True)


def main() -> None:
    """Sync entrypoint registered as the console script `mnemo-mcp`."""
    import sys

    if len(sys.argv) > 1 and sys.argv[1] in ("--version", "-V"):
        from importlib.metadata import PackageNotFoundError, version

        try:
            ver = version("m-nemo")
        except PackageNotFoundError:
            try:
                ver = version("mnemo")
            except PackageNotFoundError:
                ver = "0.1.0"
        print(f"mnemo-mcp {ver}")
        return

    asyncio.run(_bootstrap())
    mcp.run()


if __name__ == "__main__":
    main()
