@mcp.tool
@monitor_tool()
async def create_knowledge(
    title: str,
    tags: str,
    summary: str,
    content: str,
    scope: str = "global",
    project_name: str | None = None,
    source: str | None = None,
    claim_type: str | None = None,
    related: str | None = None,
    task_id: str | None = None,
) -> str:
    """Create a new knowledge entry. 适合在本次会话里产生了值得下次复用的经验
    之后调用，典型场景包括：
    - 解决了一个当下搜不到、耗了时间的棘手问题（bug 复盘、陷阱、workaround）。
    - 用户纠正了你之前的做法，并说明了原因 —— 把规则和 Why 一起记下来，
      下次才不会重犯。
    - 做出了一个非显而易见的技术决策或口径约定。
    - 发现了项目/团队内的一个事实性信息（endpoint、owner、外部系统约定）。

    写之前：
    - 先 search 同主题关键词，避免重复写入。如果已存在相近条目，
      改走 update_knowledge 或 supersede，不要叠加。
    - 用 claim_type 标明类型（fact / decision / procedure / hypothesis），
      这会影响 search 的上下文重排。
    - source 尽量填，"自己的经验"也算来源，写清楚比留空好。

    写完之后：
    - 看 response 里的 write_gate：
        exact_duplicate / title_similar / semantic_similar / evidence_weak /
        recommended_action ∈ {"create", "supersede", "review"}
      当推荐是 "supersede" 或 "review" 时，不要硬写，回头 update 匹配到的
      那条 id，或补强证据（content 太短、fact 没 source 都会被拦）。
    - 写入成功后不需要立刻通知用户"我已经记住了"，静默写入即可。

    不适合写入：
    - 当前任务独有的临时上下文（文件路径变量、一次性调试状态）。
    - 已经在 CLAUDE.md 或项目文档里显式规定的东西 —— 记忆是补充，不是复制。
    - 对用户的价值判断（"用户偏好简洁"之类带主观色彩的结论要谨慎）。

    参数：
    - tags：逗号分隔，例 "sqlite,search,中文"。
    - related：逗号分隔的已有条目 title，用来建关系边。
    - scope："global"/"project"/"session"。
    - claim_type：可选，"fact"/"decision"/"procedure"/"hypothesis"。
    - task_id：可选，搜索结果附带的任务 ID，原样传回即可。
      系统据此区分搜索派发回收 vs agent 自发反哺，用于完成率统计。

    Response includes a `write_gate` object:
        { exact_duplicate: null|{id,title},
          title_similar: [{id,title,score}],
          semantic_similar: [{id,title,cosine}],
          evidence_weak: null|{reason},
          recommended_action: "create"|"supersede"|"review" }
    """
    service = _require_service()
    clean_task_id, trigger_source = _validate_task_id(task_id)
    result = await service.create_knowledge(
        title=title,
        summary=summary,
        content=content,
        tags=_split_csv(tags),
        scope=scope,
        project_name=project_name,
        source=source,
        claim_type=_sanitize_claim_type(claim_type),
        related_titles=_split_csv(related),
        task_id=clean_task_id,
        trigger_source=trigger_source,
    )
    gate_info = result.get("write_gate") or {}
    record_payload(
        knowledge_id=result.get("id"),
        recommended_action=gate_info.get("recommended_action"),
        supersedes_id=result.get("supersedes_id"),
        scope=result.get("scope"),
        claim_type=claim_type,
        duplicate_warning=bool(result.get("duplicate_warning")),
    )
    rel = result.get("related") or []
    supersedes = result.get("supersedes_id")
    if supersedes is not None:
        header = (
            f"Created new version #{result['id']} **{result['title']}** "
            f"(supersedes #{supersedes}, version {result.get('version')})"
        )
    else:
        header = f"Created knowledge **{result['title']}** (id: {result['id']})"
    body = (
        f"{header}\n"
        f"- Tags: {_format_tags(result.get('tags') or [])}\n"
        f"- Scope: {result['scope']}"
        + (f" / {result['project_name']}" if result.get("project_name") else "")
        + f"\n- Related: {', '.join(rel) if rel else '_(none)_'}"
    )
    dup = result.get("duplicate_warning")
    if dup:
        body += (
            f"\n- **Duplicate warning:** same content already exists as "
            f"#{dup['id']} ({dup['title']})"
        )
    gate = result.get("write_gate")
    if gate:
        body += "\n\n" + _format_write_gate(gate)
    hint = hint_from_create_result(result)
    if hint:
        body += hint
    return body


def _format_write_gate(gate: dict[str, Any]) -> str:
    """Render the write_gate report into agent-friendly markdown.

    The caller is expected to read ``recommended_action`` first, then drill
    into the specific hits (exact_duplicate / title_similar / semantic_similar /
    evidence_weak / potential_contradiction) to decide whether to supersede,
    update, or review before re-writing. Action is rendered at the top so it
    doesn't get lost below detail lists.
    """
    action = gate.get("recommended_action") or "create"
    action_hint = {
        "create": "safe to keep as a new entry",
        "supersede": "same content already exists — call update_knowledge on the matched id instead",
        "review": "near-duplicate or contradiction detected — re-check before committing",
    }.get(action, "")

    lines = ["## Write-gate report"]
    if action_hint:
        lines.append(f"- **Recommended action:** `{action}` — {action_hint}")
    else:
        lines.append(f"- **Recommended action:** `{action}`")

    exact = gate.get("exact_duplicate")
    if exact:
        lines.append(
            f"- **Exact duplicate (L0):** #{exact['id']} {exact.get('title', '')}"
        )

    title_similar = gate.get("title_similar") or []
    if title_similar:
        parts = ", ".join(
            f"#{t['id']} {t.get('title', '')} ({float(t.get('score', 0)):.2f})"
            for t in title_similar
        )
        lines.append(f"- **Title similar (L1):** {parts}")

    semantic_similar = gate.get("semantic_similar") or []
    if semantic_similar:
        parts = ", ".join(
            f"#{s['id']} {s.get('title', '')} (cos={float(s.get('cosine', 0)):.2f})"
            for s in semantic_similar
        )
        lines.append(f"- **Semantic similar (L2):** {parts}")

    evidence_weak = gate.get("evidence_weak")
    if evidence_weak:
        lines.append(
            f"- **Evidence weak (L3):** {evidence_weak.get('reason', 'unknown')}"
        )

    contradictions = gate.get("potential_contradiction") or []
    if contradictions:
        parts = ", ".join(
            f"#{c['id']} {c.get('title', '')} ({c.get('reason', '')})"
            for c in contradictions
        )
        lines.append(f"- **Potential contradiction (L4):** {parts}")

    return "\n".join(lines)


@mcp.tool
@monitor_tool()
async def search(
    query: str,
    scope: str | None = None,
    project_name: str | None = None,
    limit: int = 20,
    include_archived: bool = False,
    task_context: str | None = None,
    mode: str = "hybrid",
    sort_by: str = "relevance",
) -> str:
    """Full-text search across stored knowledge. This is the first thing to reach
    for when a task touches territory that might have been solved before —
    接到新任务、不确定某个决定是否已有定论、用户追问"我们上次怎么处理的"、
    或准备写一段新经验前都应该先 search 一次。查重是 create_knowledge 的
    隐含前置步骤。

    用完之后：
    - 命中了有用结果 → 直接用结果回答用户，并对用到的那条调用
      feedback_knowledge(signal="helpful")，让排序学到正向信号。
    - 命中了但跟当下场景不符 / 已过时 → 对那条调用
      feedback_knowledge(signal="misleading" 或 "outdated")，并考虑
      update_knowledge 或 archive_knowledge。
    - 摘要不够判断 → 用 get_knowledge 取完整内容，不要凭 summary 猜。
    - 没命中 → 视任务类型决定是解决它再 create_knowledge 记录下来，还是
      放弃搜索、直接做。如果长 query 搜不到，试试缩短到 2-3 个核心词重搜；
      系统会自动尝试缩短重试，但手动缩短效果更精准。

    不建议用在：
    - 用户明确说"不要翻历史，按我说的做" —— 这时候翻记忆只会增加噪声。
    - 纯事实闲聊（今天星期几之类）。
    - 已经在上一次 search 结果里了还反复搜同一个 query。

    参数：
    - scope：可选，"global"/"project"/"session" 过滤。
    - project_name：scope="project" 时的项目名。
    - include_archived：默认隐藏归档条目；排查旧决策时可打开。
    - task_context：可选，"coding"/"debug"/"decision"/"onboarding"/"general"，
      打开 context-aware 重排。传未知值会降级为 "general" 并告警。
    - mode：可选，"hybrid"（默认）/"fts"/"vector"。hybrid 同时走关键词+
      语义+图谱融合排序。fts 纯关键词匹配，适合精确查找。vector 纯语义
      搜索，适合模糊意图。一般不需要手动指定。
    - sort_by：可选，"relevance"（默认）/"time"/"feedback"。relevance 按
      综合相关度排序。time 按创建时间从新到旧。feedback 按使用反馈权重
      排序（被标记 helpful 越多排越前）。
    """
    service = _require_service()
    hits = await service.search(
        query,
        scope=scope,
        project_name=project_name,
        limit=limit,
        mode=mode,
        include_archived=include_archived,
        task_context=task_context,
        sort_by=sort_by,
    )
    top1 = hits[0] if hits else None
    record_payload(
        hits=len(hits),
        mode=mode,
        sort_by=sort_by,
        task_context=task_context,
        scope=scope,
        include_archived=include_archived,
        auto_fallback=bool(top1 and top1.get("auto_fallback")),
        top1_id=(top1 or {}).get("id"),
        top1_score=(top1 or {}).get("rrf_score"),
    )
    if not hits:
        body = (
            f'No results for "{query}". This topic has no prior knowledge yet. '
            f"After you finish this task, consider calling `create_knowledge` "
            f"to record what you learned — the next agent will thank you."
        )
    else:
        lines = [f'## Search Results for "{query}"  ({len(hits)} hits)', ""]
        for i, h in enumerate(hits, start=1):
            lines.append(_format_summary_entry(i, h))
        body = "\n".join(lines)

    task = pick_task_for_search(
        project_name=project_name,
        query=query,
        scope=scope,
    )
    if task:
        body += format_task_hint(task)
    return body


@mcp.tool
@monitor_tool()
async def get_knowledge(id_or_title: str) -> str:
    """Fetch the full text of a knowledge entry by numeric id or exact title.
    search 返回的是摘要，真正要"照着办"或"引用 Why"之前，应该先用
    get_knowledge 把完整内容读出来。

    典型使用时机：
    - search 命中的摘要里提到"see content for details / 完整步骤见正文" ——
      不读正文就执行基本会漏步骤。
    - 知识的 claim_type 是 "decision" 或 "procedure"，需要理解前因后果
      而不仅是结论。
    - 用户追问"为什么是这样" —— Why 一般在 content 里，不在 summary 里。
    - 准备 update_knowledge 之前要先读原文，看哪些字段值得保留、哪些要
      重写。
    - 准备写一条看起来相关的新知识时，先读一下老条目，决定是补充还是新建。

    不需要 get_knowledge 的情形：
    - search 摘要已经足够精确回答用户的问题。
    - 条目 title 暗示它只是个指针（"See X for details"）而目标 X 已经在手。
    - 只是想列一下相关条目标题 —— 用 get_related / search_by_tag 更轻。

    参数：
    - id_or_title：整数 id 或精确 title。title 必须一字不差；
      模糊匹配走 search。

    返回：完整 markdown，包括 tags / scope / source / related / 时间戳 /
    summary / content。
    """
    service = _require_service()
    key = _parse_id_or_title(id_or_title)
    result = await service.get_knowledge(key)
    if result is None:
        record_payload(found=False, lookup_key_type="id" if isinstance(key, int) else "title")
        return f"Knowledge not found: {id_or_title}"
    record_payload(
        found=True,
        knowledge_id=result.get("id"),
        lookup_key_type="id" if isinstance(key, int) else "title",
        claim_type=result.get("claim_type"),
        scope=result.get("scope"),
    )
    return _format_full_entry(result)


@mcp.tool
@monitor_tool()
async def update_knowledge(
    id: int,
    title: str | None = None,
    tags: str | None = None,
    summary: str | None = None,
    content: str | None = None,
    task_id: str | None = None,
) -> str:
    """Update fields on an existing knowledge entry. 适合在原有知识基础上
    做增量修正 —— 补充遗漏、纠正错字、加更多 tag、扩写 summary。
    语义级的变化（结论反了、适用场景变了）由 service 层判定是否触发
    supersede 新版本，调用方不需要自己分辨。

    典型使用时机：
    - feedback 标了 "misleading" 之后，补强 content 把 Why 说清楚。
    - feedback 标了 "outdated" 但只是细节过期（比如路径变了），
      原则仍然成立 —— 修细节比重写快。
    - 新增 related 关系，把这条和新写的知识连起来。
    - tag 体系演化，把老条目也补上新 tag 方便检索。

    写之前：
    - 先 get_knowledge 读一遍原文，决定"保留什么、改什么"，不要凭
      summary 就改 content。
    - 想好：这是小修还是结论反转？如果是反转，直接 update 是合理的，
      service 会根据变化幅度决定是否产生新版本（supersedes_id）。

    写之后：
    - 看返回里有没有 supersedes_id —— 有的话说明新建了一条版本链，旧
      那条仍然可查；没的话是原地修改。
    - 不需要再 create 一条"已修正"说明。

    只传你想改的字段。完全不传字段会直接返回 "No fields to update."。

    参数（全部可选）：
    - title / tags / summary / content。tags 用逗号分隔。
    - task_id：可选，搜索结果附带的任务 ID，原样传回即可。
      系统据此统计 search 派发的维护任务被实际执行的比例。
    """
    service = _require_service()
    clean_task_id, trigger_source = _validate_task_id(task_id)
    fields: dict[str, Any] = {}
    if title is not None:
        fields["title"] = title
    if tags is not None:
        fields["tags"] = _split_csv(tags)
    if summary is not None:
        fields["summary"] = summary
    if content is not None:
        fields["content"] = content
    if not fields:
        record_payload(knowledge_id=id, noop=True, fields_changed=0)
        return "No fields to update."

    if clean_task_id is not None:
        fields["task_id"] = clean_task_id
    fields["trigger_source"] = trigger_source
    result = await service.update_knowledge(id, **fields)
    rel = result.get("related") or []
    supersedes = result.get("supersedes_id")
    record_payload(
        knowledge_id=result.get("id", id),
        supersedes_id=supersedes,
        fields_changed=len(fields),
        changed_keys=sorted(fields.keys()),
    )
    header = (
        f"Created new version #{result['id']} **{result['title']}** "
        f"(supersedes #{supersedes}, version {result.get('version')})"
        if supersedes is not None
        else f"Updated knowledge **{result['title']}** (id: {result['id']})"
    )
    return (
        f"{header}\n"
        f"- Fields changed: {', '.join(fields.keys())}\n"
        f"- Tags: {_format_tags(result.get('tags') or [])}\n"
        f"- Related: {', '.join(rel) if rel else '_(none)_'}"
    )


